package com.creditharmony.core.common.entity;

import com.creditharmony.core.persistence.DataEntity;

/**
 * 数据域实体
 * @Class Name DataDomain
 * @author 张永生
 * @Create In 2015年12月19日
 */
public class DataDomain extends DataEntity<DataDomain> {

	private static final long serialVersionUID = 7218265748011125506L;
	
	private String domainType;           // 数据域的类型,例如org
	private String domainId;             // 数据域的id,例如组织机构表的id
	private String sourceType;           // 源数据的类型,例如user
	private String sourceId;             // 源数据的id,例如用户表的id
	
	public String getDomainType() {
		return domainType;
	}
	public void setDomainType(String domainType) {
		this.domainType = domainType;
	}
	public String getDomainId() {
		return domainId;
	}
	public void setDomainId(String domainId) {
		this.domainId = domainId;
	}
	public String getSourceType() {
		return sourceType;
	}
	public void setSourceType(String sourceType) {
		this.sourceType = sourceType;
	}
	public String getSourceId() {
		return sourceId;
	}
	public void setSourceId(String sourceId) {
		this.sourceId = sourceId;
	}
	
	
}
