package com.creditharmony.core.common.entity;

/**
 * 业务流水号
 * @Class Name BusinessSeq
 * @author 陈伟东
 * @Create In 2015年12月11日
 */
public class BusinessSeq {

	//流程类型
	private String flowType;
	//int类型的日期
	private int day;
	//流水号
	private int seq;
	
	public String getFlowType() {
		return flowType;
	}
	public void setFlowType(String flowType) {
		this.flowType = flowType;
	}
	public int getDay() {
		return day;
	}
	public void setDay(int day) {
		this.day = day;
	}
	public int getSeq() {
		return seq;
	}
	public void setSeq(int seq) {
		this.seq = seq;
	}
}
