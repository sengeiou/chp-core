package com.creditharmony.core.permission.entity;

import com.creditharmony.core.persistence.DataEntity;

/**
 * 额外权限控制entity
 * 
 * @author chenwd
 *
 */
public class AdditionalPermission extends DataEntity<AdditionalPermission> {
	
	public static String TYPE_ORG = "org";
	public static String STATUS_OPEN = "0";	//启用
	public static String SCOPE_10 = "10";	//含子机构
	public static String SCOPE_11 = "11";	//仅子机构
	public static String SCOPE_12 = "12";	//仅本机构

	private static final long serialVersionUID = 1L;
	
	private String id;
	private String type;
	private String status;
	private String scope;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getStatus() {
		return status;
	}
	public void setStatus(String status) {
		this.status = status;
	}
	public String getScope() {
		return scope;
	}
	public void setScope(String scope) {
		this.scope = scope;
	}

}
