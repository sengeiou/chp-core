package com.creditharmony.core.users.util;

import com.creditharmony.core.common.type.OperationType;


/**
 * 操作帮助类
 * @Class Name OperationHelper
 * @author 张永生
 * @Create In 2015年11月5日
 */
public class OperationHelper {

	/**
	 * 判断菜单修改页面上的“所属模块菜单”是否禁用
	 * 2016年1月15日
	 * By 张永生
	 * @param flag
	 * @return
	 */
	public static boolean isModuleMenuDisabled(Integer flag){
		 boolean disabled = false;
		 if(OperationType.OPERATE_EDIT.equals(flag)){
			 disabled = true;
		 }
		 return disabled;
	}
}
